
# JOIN TUPLES

tuple1 = ("apple","banana","cherry")
tuple2 = ("mango","orange","grapes")
combined = tuple1+tuple2
print(combined)           # ('apple', 'banana', 'cherry', 'mango', 'orange', 'grapes')



# Repeat tuple
       # You can also multiply a tuple to repeat its items:

fruits = ("apple","banana")
print(fruits *3)        # ('apple', 'banana', 'apple', 'banana', 'apple', 'banana')