# Type Casting
      # type casting is a tkind of explicit type conversion, where the programmer manually changes the data type using bult-in functions.

x = int(3.99)    
y = float(1)     
z = complex(4)   
a = str(4)     
print(x)           # 3
print(y)           # 1.0
print(z)           # (4+0j)
print(repr(a))     # '4'


# INTEGER
x = int(2)
y = int(3.8)
z = int("3")
print(x)      # 2
print(y)      # 3
print(z)      # 3


# FLOATS
x = float(1)
y = float(2.6)
z = float("3")
w = float("4.2")
print(x)      # 1.0
print(y)      # 2.0
print(z)      # 3.0
print(w)      # 4.2


# STRING
x = str("s1")
y = str(2)
z = str(3.0)
print(x)      # s1
print(y)      # 2
print(z)      # 3.0


# COMPLEX
x = complex(2)
y = complex(10,4)
z = complex(2+8)
print(x)      # (2+0j)
print(y)      # (10+4j)
print(z)      # (10+0j)

