
# ASSIGNMENT OPERATORS 
   # Assignment operators are used to assign values to variables.

# HERE ARE SOME EXAMPLES:

#  = 
x = 5
print(x)    # 5

# +=
x = 3
x += 5
print(x)  # 8

# -=
x = 3
x -= 5
print(x)   # -2

# *=
x = 3
x *= 3
print(x)   # 9

# /=
x = 5
x /= 3
print(x)  # 1.666666 

# %=
x = 5
x %= 3
print(x)  # 2

# //=
x = 5
x //= 3
print(x)  # 1

# **=
x = 5
x **= 3
print(x)     # 125

# &=
x = 5
x &= 3
print(x)   # 1

# |=
x = 5
x |= 3
print(x)   # 7

# ^=
x = 5
x ^= 3
print(x)   # 6

# >>=
x = 5
x >>= 3
print(x)  # 0

# <<=
x = 5
x <<= 3
print(x)   # 40

# :=
print(x := 5)   # 5
