
# COMPARISION OPERATORS
     # Compasrision operators are used to compare two values:


# Comparision types :
   # Equal
   # Not Equal
   # Greater than
   # Less than
   # Greater than or Equal to
   # Less than or Equal to


# EQUAL ( == )
x = 5
y = 3
print(x ==y )   # False


# NOT EQUAL ( != )
x = 5
y = 6
print(x != y)   # True


# GREATER THAN ( > )
x = 7
y = 3
print(x > y)  # True


# LESS THAN ( < )
x = 7
y = 3
print(x < y)   # False


# GREATER THAN OR EQUAL TO ( >= )
x = 7
y = 8 
print(x >= y)   # False


# LESS THAN OR EQUAL TO ( <= )
x = 7
y = 9
print(x <= y)   # True






