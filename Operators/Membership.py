
# MEMBERSHIP OPERATORS
      # Membership operators are used to test, if a sequence is presented in an object.

# in      = Returns True, if a sequence with the specified value is present in the object.  x in y
# not in  = Returns True, if a sequence with the specified value is not present in the object.   x not in y


# in
x = "now, i am praticing python"
print( "i" in x)         # True  , because a sequence with the value "i" is in the list. 
print("chenna" in x)     # False


# Not in
x = "now, i am praticing python"
print("chenna" not in x)   # True , because a sequence with the value "chenna" is not in the list.  
print("i" not in x)       # False