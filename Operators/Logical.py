
# LOGICAL OPERATORS


# and : Returns True only if both expressions are True.
# or  : Returns True if at least one of the expressions is True.
# not : Reverse the result.


# and
x = 5
y = 8
print(x > y and x > 15)      # False
print(x > 5 and y < 34)      # False


# or
x = 5
y = 3
print(x > 10 or y > 10)      # False
print(x > 10 or y < 10)      # True


# Not
x = 7
print(not(x > 5))         # False
print(not(x < 5))         # True



